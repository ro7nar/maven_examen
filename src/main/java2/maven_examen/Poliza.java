package maven_examen;

public class Poliza {

	private String nombre;
	private String descripcion;
	private int anoVigencia;
	private int codigoPoliza;
	private int valorPoliza;
	private int codigoDescuento;
	private int numeroSeguro;
	private int beneficio;
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	//Metodos SET y GET
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public int getAnoVigencia() {
		return anoVigencia;
	}
	public void setAnoVigencia(int anoVigencia) {
		this.anoVigencia = anoVigencia;
	}
	public int getCodigoPoliza() {
		return codigoPoliza;
	}
	public void setCodigoPoliza(int codigoPoliza) {
		this.codigoPoliza = codigoPoliza;
	}
	public int getValorPoliza() {
		return valorPoliza;
	}
	public void setValorPoliza(int valorPoliza) {
		this.valorPoliza = valorPoliza;
	}
	public int getCodigoDescuento() {
		return codigoDescuento;
	}
	public void setCodigoDescuento(int codigoDescuento) {
		this.codigoDescuento = codigoDescuento;
	}
	public int getNumeroSeguro() {
		return numeroSeguro;
	}
	public void setNumeroSeguro(int numeroSeguro) {
		this.numeroSeguro = numeroSeguro;
	}
	public int getBeneficio() {
		return beneficio;
	}
	public void setBeneficio(int beneficio) {
		this.beneficio = beneficio;
	}
	
}
